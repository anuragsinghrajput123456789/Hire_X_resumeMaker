const generateResumeFromImage = async (req, res) => {
    try {
        const { image, data } = req.body;
        if (!image) return res.status(400).json({ error: 'Image required' });
        
        // Image handling in REST API requires "inlineData" part
        const matches = image.match(/^data:([a-zA-Z0-9]+\/[a-zA-Z0-9-.+]+);base64,/);
        const mimeType = matches ? matches[1] : 'image/jpeg';
        const base64Image = image.replace(/^data:([a-zA-Z0-9]+\/[a-zA-Z0-9-.+]+);base64,/, '');

        const payload = {
            contents: [{
                parts: [
                    { text: `Create HTML resume matching this image layout but using this data: ${JSON.stringify(data)}` },
                    { inlineData: { mimeType: mimeType, data: base64Image } }
                ]
            }]
        };

        const response = await fetch(API_URL, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(payload)
        });

        if (!response.ok) {
             const t = await response.text();
             throw new Error("Gemini Image API Error: " + t);
        }
        
        const d = await response.json();
        let html = d.candidates?.[0]?.content?.parts?.[0]?.text;
        
        if (html) {
             // Cleanup markdown
            if (html.startsWith('```html')) html = html.replace(/^```html\n/, '').replace(/\n```$/, '');
            else if (html.startsWith('```')) html = html.replace(/^```\n/, '').replace(/\n```$/, '');
            res.json({ result: html });
        } else {
            throw new Error("No HTML generated");
        }
    } catch(e) {
        console.error(e);
        res.status(500).json({error: 'Failed to generate resume from image'});
    }
}

