const analyzeResumeRealTime = async (req, res) => {
    try {
        const { resumeText, jobRole } = req.body;
        const cleanedText = cleanResumeText(resumeText).substring(0, 15000);
        
        if (cleanedText.length < 100) return res.status(400).json({ error: 'Resume text is too short.' });
        
        const expectedKeywords = getJobRoleKeywords(jobRole);
        const prompt = `Analyze this resume for the "${jobRole}" position. Return ONLY JSON.
Resume:
${cleanedText}
Target Role: ${jobRole}
Expected Keywords: ${expectedKeywords.join(', ')}

Required JSON Structure:
{
  "keywordMatchScore": <number 0-100>,
  "foundKeywords": [list strings],
  "missingKeywords": [list strings],
  "readabilityScore": <number 0-100>,
  "structureAnalysis": {
    "Contact Information": <bool>,
    "Professional Summary": <bool>,
    "Work Experience": <bool>,
    "Education": <bool>,
    "Skills": <bool>,
    "Projects": <bool>
  },
  "formattingIssues": [list strings]
}`;

        const text = await callGemini(prompt, 0.2);
        const jsonMatch = text.match(/\{[\s\S]*\}/);
        const parsed = jsonMatch ? JSON.parse(jsonMatch[0]) : null;
        
        if (parsed) {
            res.json(parsed);
        } else {
             // Fallback
             throw new Error("Failed to parse JSON response");
        }
    } catch (error) {
        console.error('Real-time analysis error:', error);
        // Fallback response logic could be added here if needed, but passing error for now
        res.status(500).json({ error: 'Failed to analyze resume' });
    }
};

